def str_upper():
    """Какой то докстринг."""
    user_input = input("Введите слово, я преобразую все буквы к заглавным")
    return user_input.upper()


def str_capitalize():
    """Преобразует первые буквы к заглавным"""
    user_input = input("Введите слово, я преобразую первые буквы к заглавным")
    return user_input.capitalize()
